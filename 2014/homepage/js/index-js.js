var m=0;
var n=1000;
function like(){
	if(m==0){
		document.getElementById("likeme-icon").innerHTML='<i class="iconfont" style="font-size:90px;">&#xf0004;</i>';
		document.getElementById("likeme-num").innerHTML=n+1;
		document.getElementById("likeme-feedback").innerHTML="谢谢你(*。∇^*)";
	}
	if(m==2){
		document.getElementById("likeme-feedback").innerHTML="你这么喜欢我真的好吗>///<";
	}
	if (m==1) {
		document.getElementById("likeme-num").innerHTML=n+1;
		document.getElementById("likeme-feedback").innerHTML="原谅你辣(￣Д￣)ﾉ";
	};
	m=2;
}
function dislike(){
	if(m==0){
		document.getElementById("likeme-icon").innerHTML='<i class="iconfont" style="font-size:90px;">&#xf0004;</i>';
		document.getElementById("likeme-num").innerHTML=n+2;
		document.getElementById("likeme-feedback").innerHTML="被骗了吧(*。∇^*)";
	}
	if(m==1){
		document.getElementById("likeme-num").innerHTML=n*2;
		document.getElementById("likeme-feedback").innerHTML="哼(￣へ￣)";
	}
	if(m==2){
		document.getElementById("likeme-num").innerHTML=n*2;
		document.getElementById("likeme-feedback").innerHTML="哼你怎么可以变心(＃°Д°)";
	}
	m=1;
	n=n*2;
}